// lastIndexOf()
/*
The lastIndexOf() method in Javascript is used to find the last occurrence of a specified
element in an array. It retirns the index of that element's last occurrence, or -1 
if the element is not found.

*/

let number = [1, 2, 3, 4, 3, 5];

//Find the last occurrence of the number 3
let lastIndex = number.lastIndexOf(3);
console.log(lastIndex); //4

